#ifndef SYS_STDINT_H
#define SYS_STDINT_H

/**
 * @brief Standard uint types.
 * 
 */
typedef unsigned char          uint8_t;
typedef unsigned short int     uint16_t;
typedef unsigned long int      uint32_t;
typedef unsigned long long int uint64_t;

/**
 * @brief Size types.
 * 
 */
typedef uint32_t size_t;

#endif // SYS_STDINT_H
